                جزء زمان مشاوره محسوب خواهد شد که این زمان حداقل ۳۰ دقیقه محاسبه می شود.
            </p>
            <p>
                10-3 حق المشاوره فقط در خصوص موضوع مورد درخواست می‌باشد و چنانچه در جریان جلسه مشاوره موضوعات و پرونده های دیگری از سوی کاربر مطرح گردد، مشاوره
