            <h3 class="font-bold text-xl">ماده ۴ – هزینه ها و پرداخت ها</h3>
            <p>
                1-4 کلیه پرداخت های کاربران از طریق کارت های عضو شتاب و درگاه های موجود در وبسایت گروه وکلای آگه انجام می پذیرد.
            </p>
            <p>
                2-4 کاربر می پذیرد با توجه به اینکه اعلام هزینه توسط وب سایت، ایجاب محسوب می گردد و قبول یا رد آن توسط کاربر کاملا اختیاری می باشد. لذا روابط فی
                مابین به عنوان قرارداد خصوصی تعریف و تفسیر می گردد و تفاوت هزینه خدمات با تعرفه های قانونی مطابق ماده ۱۰ قانون مدنی نافذ است.
            </p>
            <p>
                3-4 کاربر می پذیرد نسبت به هزینه و مبالغ تعیین شده جهت ارائه خدمات هیچ گونه اعتراضی نداشته و در صورت عدم رضایت از هزینه های اعلامی مختار به عدم
                رزرو وقت می باشد و در صورت رزرو وقت هزینه های آن را پرداخت نماید.
            </p>
            <p>
                4-4 درصورتی‌ که کاربر مبلغ مشاوره خویش را پرداخت کرده ولی نتواند از خدمات وبسایت گروه وکلای آگه استفاده نماید و این امر منتسب به سامانه باشد،
                گروه وکلای آگه موظف است قبل از زمان مشاوره مشکل پیش آمده را به اطلاع کاربر رسانده و وقت دیگری را برای وی در نظر بگیرد.
            </p>
            <p>
                5-4 کاربر می پذیرد درصورت پرداخت ناموفق از طریق درگاه بانکی، شخصا مسئول پیگری وجه کسر شده از سیستم بانکی مربوطه باشد و از نظر گروه وکلای آگه،
                وجه مذکور پرداخت شده محسوب نمی گردد و گروه وکلای آگه در این خصوص مسئولیتی ندارد.
            </p>
