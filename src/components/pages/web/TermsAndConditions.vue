                تعیین شده نسبت به برقراری تماس از طریق راه های ارتباطی اعلام شده (متنی یا تماس از طریق سایت) اقدام نماید. در صورت عدم اقدام لازم از سوی کاربر در
                زمان مقرر، وجه پرداختی بابت هزینه مشاوره قابل استرداد نمی باشد و دلایلی چون قطعی اینترنت،عدم اعتبارکافی تلفن همراه، عدم شارژ باطری تلفن همراه،
