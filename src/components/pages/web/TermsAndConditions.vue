            <h3 class="font-bold text-xl">ماده ۳ - استفاده از خدمات</h3>
            <p>
                1-3 شامل بانک اسناد حقوقی و قراردادها، ، امکان درخواست تنظیم و بررسی قراردادهای حقوقی و پشتیبانی از آنان، درخواست مشاوره حقوقی تخصصی حضوری و
                غیرحضوری و درخواست استفاده از خدمات وکیل برای اشخاص حقیقی، حقوقی و کسب و کارها است.
            </p>
            <p>
                2-3 مشاوره حقوقی صورت گرفته در وبسایت چنانچه به صورت پیامی باشد، متن پیام ها در حساب کاربری کاربر ذخیره شده و کاربر می تواند هر زمان با ورود به
                حساب کاربری خود به این محتوای حقوقی دسترسی پیدا کند.
            </p>
            <p>
                3-3 اگر تراکنش پرداخت هزینه توسط عامل پرداخت (خارج از کنترل سامانه) برگشت بخورد، یا دچار مشکلی شود یا مطابق قوانین، دریافت خدمات لغو شود هزینه
                کاربر عودت داد میشود و رزرو وقت مشاوره و دریافت خدمات حقوقی معلق یا ملغی خواهد شد. اگر به هر دلیلی تراکنش مشکوک و غیرقانونی اتفاق بیافتد،
                تراکنش‌های انجام شده لغو و باطل خواهد شد و رزرو وقت مشاوره و دریافت خدمات حقوقی معلق یا ملغی خواهد شد.
            </p>
            <p>
                4-3 کاربر متعهد می‌شود هیچ‌گاه از خدمات بیان امروز به صورتی که به سامانه یا شرکای آن صدمه‌ای وارد نماید و در راستای اهداف غیرقانونی استفاده
                نکند. در صورتی که کاربر نسبت به سوءاستفاده از امکانات فراهم شده در خدمات سامانه در جهت منافع شخصی و یا آسیب مادی و یا معنوی به منافع گروه وکلای
