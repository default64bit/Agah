            <h3 class="font-bold text-xl">ماده ۵ – مسئولیت ها و رفع مسئولیت های سامانه</h3>
            <p>
                1-5 تمامی اطلاعات کاربربه عنوان اطلاعات محرمانه نزد گروه وکلای آگه محافظت شده و دسترسی به آن توسط اشخاص ثالث ممنوع بوده مگر برابر تصمیم مقام
                ذیصلاح قضایی. بنابر این در صورت درخواست و صدور دستور مقتضی از سوی مراجع قضایی، سامانه مکلف به ارائه اطلاعات کاربر به مراجع مذکور می‌باشد.
            </p>
            <p>
                2-5 گروه وکلای آگه تلاش معقول می کند که محتوا یا دسترسی کاربر به وبسایت بدون وقفه و مشکل باشد، ایرادها در صورت وجود داشتن رفع شوند، و محتوای
                وبسایت عاری از ویروس و دیگر اجزاء مخرب باشد، اما این موارد قابل تضمین نیست؛ بلکه تلاش معقول انجام می شود که دسترسی به وبسایت برقرار باشد، و
                وبسایت و محتوای آن از نظر صحت، دقت، کامل بودن، قابل اعتماد بودن، و موارد مشابه قابل قابل اعتماد باشد.
            </p>
            <p>
                3-5 سامانه سعی می کند دسترسی شبانه روزی در تمامی ۷ روز هفته برای کاربر برقرار باشد. ممکن است به دلیل تعمیر و نگهداری وبسایت، یا دلایل و مشکلات
                فنی دیگر لازم باشد دسترسی به وبسایت یا ارائه خدمات و محتوای وبسایت موقتاً متوقف یا معلق شود، ولی گروه وکلای آگه تمام سعی خود را برای برطرف کردن
                موارد فوق خواهد کرد.
            </p>
            <p>
                4-5 گروه وکلای آگه در خصوص خسارات ناشی از موارد زیر از خود سلب مسئولیت می نماید
            </p>
            <p>
                5-4-1 خساراتی که ناشی از اقدامات خارج از سامانه باشد.
            </p>
            <p>
                2-4-5 خساراتی که از هک یا نفوذ غیرمجاز به سامانه گروه وکلای آگه باشد.
            </p>
            <p>
                3-4-5 خساراتی که ناشی از ارائه هرگونه اطلاعات ناقص یا نادرست و یا اقدامات زیانبار کاربر باشد.
            </p>
            <br />

            <h3 class="font-bold text-xl">ماده ۶- مسئولیت های کاربر</h3>
            <p>
                1-6 مسئولیت همۀ اقدامات و افعال کاربر که ناشی از عدم رعایت قوانین موضوعه جمهوری اسلامی ایران از جمله قانون مجازات اسلامی (ارتکاب جرم) و قوانین
                مدنی (ورود خسارت) باشد، بر عهدۀ شخص متخلف بوده و گروه وکلای آگه هیچ گونه مسئولیتی در قبال اعمال و افعال فوق نداشته و نخواهد داشت و گروه وکلای
                آگه می‌تواند ضمن غیرفعال کردن حساب کاربری متخلف، از طریق مراجع ذیصلاح، اقدامات قانونی را علیه نامبرده جهت احقاق حقوق قانونی این گروه به عمل
                آورد.
            </p>
            <p>
                2-6 در صورتی که لینک وبسایت دیگری درون هر یک از بخشهای سامانه گروه وکلای آگه وجود داشته باشد به معنی تایید فعالیت ها و محتویات آن سایت نمی باشد.
                هرگونه تراکنش مالی یا تعهد احتمالی از سوی آن سایت بر عهده پدید آورندگان آن سایت می باشد و مسئولیت استفاده از خدمات آن سایت تنها متوجه شخص کاربر
                است.
            </p>
            <p>
                3-6 هرگونه اقدامی از سوی کاربر که منجر به ورود خسارت به شهرت، اعتبار و دارایی‌های گروه وکلای آگه شود تخلف از شرایط قراردادی و قانونی تلقی خواهد
                شد. سامانه این حق را برای خود محفوظ می‌داند که حساب کاربر را مسدود کند و در صورت نیاز، علیه شخص مذکور اقدام قانونی انجام دهد و خسارت و حقوق
                قانونی خود را مطالبه کند.
            </p>
            <br />

            <h3 class="font-bold text-xl">ماده ۷ – موارد فنی</h3>
            <p>
                1-7 کاربر حق ندارد هیچ گونه تلاشی در جهت استخراج سورس کد نرم افزارهای گروه وکلای آگه شامل دیکامپایل (Decompile)، مهندسی معکوس (Reverse
                Engineering) یا فعالیت‌های مشابه انجام دهد.
            </p>
            <p>
                2-7 کاربر مجاز نیست با استفاده از روش‌های Framing یا Mirroring یا روش‌های دیگر نسخه دیگری از سرویس گروه وکلای آگه را روی سرورهای دیگر ارائه
                نماید. همچنین کاربر مجاز نیست در نرم‌افزار یا در روش ارائه سرویس گروه وکلای آگه تغییر یا اخلالی ایجاد کند.
            </p>
            <p>
                3-7 کاربر حق ندارد برنامه یا اسکریپتی با هدف ایندکس گذاری، مطالعه یا هرگونه فعالیت داده کاوی روی سرویس اجرا کند.
            </p>
            <p>
                4-7 کاربر حق ندارد هیچ فعالیتی در راستای حصول دسترسی غیرقانونی و غیرمتداول به هیچ بخش سرویس گروه وکلای آگه یا سرویس های مرتبط با آن انجام دهد.
